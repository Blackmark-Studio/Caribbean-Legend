void ProcessDialogEvent()
{
	ref NPChar, d;
	aref Link, Diag;
	string NPC_Meeting;

	DeleteAttribute(&Dialog,"Links");

	makeref(NPChar,CharacterRef);
	makearef(Link, Dialog.Links);
	makeref(d, Dialog);
	makearef(Diag, NPChar.Dialog);

	switch(Dialog.CurrentNode)
	{
		case "exit":
			Diag.CurrentNode = Diag.TempNode;
			DialogExit();
		break;
		
		case "prepare_convoy_quest":
			if (isBadReputation(pchar, 40)) 
			{
				dialog.text = RandPhraseSimple("啊, 见鬼! 我知道你是谁了! 只有十足的傻瓜才会雇你护送船只。 再见! ", "哦, 我知道你是谁了! 你说雇你当护卫? 我可不是傻瓜... ");
				link.l1 = RandPhraseSimple("呵, 真遗憾! ", "如今的人确实太容易害怕了! ");
				link.l1.go = "convoy_refused";
			}
			else
			{
				dialog.text = TimeGreeting() + ", " + GetAddress_Form(NPChar) + "! 我是" + GetFullName(NPChar) + ", 一名商人。 我听说你在找工作? ";
				link.l1 = "差不多。 而且, 如我所闻, 你在寻找能护送你和你的船到达目的地的船长? ";
				link.l1.go = "prepare_convoy_quest_2";
			}
		break;
		
		case "prepare_convoy_quest_2":
			dialog.text = "正是。 此外, 我认为你正是适合担任我护卫的人选。 你觉得如何? ";
			link.l1 = "好吧, 给我开个价, 或许我们能成交。 ";
			link.l1.go = "prepare_convoy_quest_3";
		break;
		
		case "prepare_convoy_quest_3":
			LookShipConvoy();
			GenerateConvoyQuest(npchar);
			dialog.text = "我需要被护送到" + XI_ConvertString("Colony" + pchar.quest.destination + "Gen") + ", 它位于" + XI_ConvertString(GetIslandByCityName(pchar.quest.destination) + "Dat") +
				", 在" + FindRussianDaysString(sti(pchar.ConvoyQuest.iDay)) + ", 为此我将支付你" + FindRussianMoneyString(sti(pchar.ConvoyQuest.convoymoney)) + "。 那么, 你的决定是? ";
			link.l1 = "我接了。 ";
			link.l1.go = "convoy_agreeded";
			link.l2 = "我觉得这提议没什么意思。 ";
			link.l2.go = "convoy_refused";
		break;
		
		case "convoy_refused":
			chrDisableReloadToLocation = false;
			npchar.LifeDay = 0;
			Diag.CurrentNode = Diag.TempNode;
			DialogExit();
			AddDialogExitQuest("convoy_refused");
		break;
		
		case "convoy_agreeded":
			chrDisableReloadToLocation = false;
			pchar.convoy_quest = pchar.quest.destination;
			Diag.CurrentNode = Diag.TempNode;
			DialogExit();
			AddDialogExitQuest("convoy_agreeded");
		break;
		
		case "complete_convoy_quest":
			dialog.text = "哦! 谢谢你。 在你的保护下, 我前所未有的感到安全。 这是你应得的报酬。 ";
			Link.l1 = "不客气。 ";
			link.l1.go = "exit";
			//传闻
			AddSimpleRumour(LinkRandPhrase("一位名叫" + GetFullName(npchar) + "的商船船长说, 当需要护卫时, 可以信任" + GetMainCharacterNameDat() + "船长。 ", 
				"名为" + GetFullName(npchar) + "的商人说" + GetMainCharacterNameDat() + "船长值得信赖。 " + GetSexPhrase("他","她") + "在护送他到" + XI_ConvertString("Colony" + pchar.quest.destination + "Gen") + "时, 以最佳方式保护了他的船只。 ", 
				"我听说你信守对请求护卫的商船船长的承诺。 一位名叫" + GetFullName(npchar) + "的商人对你评价很高。 "), sti(npchar.nation), 40, 1);
			pchar.quest.generate_convoy_quest_progress = "completed";
			chrDisableReloadToLocation = false;
			npchar.LifeDay = 0;
			AddDialogExitQuest("convoy_refused");
		break;
	}
} 

